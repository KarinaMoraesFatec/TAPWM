let primeiroNum = prompt("Digite o primeiro número:");
let segundoNum = prompt("Digite o segundo número:");

soma =  parseInt(primeiroNum) +  parseInt(segundoNum);
subtracao =  parseInt(primeiroNum) -  parseInt(segundoNum);
produto =  parseInt(primeiroNum) *  parseInt(segundoNum);
divisao =  parseInt(primeiroNum) /  parseInt(segundoNum);
restoDivisao = parseInt(primeiroNum) %  parseInt(segundoNum);


alert("O resultado da soma é: " + soma);
alert("O resultado da subtração é: " + subtracao);
alert("O resultado da produto é: " + produto);
alert("O resultado da divisão é: " + divisao);
alert("O resto da divisão é: " + restoDivisao);